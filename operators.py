# Arithmetic operators - Simple calculations

num1 = 10
num2 = 3

print(num1+num2)
print(num1*num2)
print(num1/num2)
print(num1%num2) # Modulus - returns the remainder after division

# Comparison OPerators - Compares values
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) # Equal to
print(num1 != num2)   # Not equal to

# Assignment operators -assigns values to variables
x =100
print(x)

x += 2
print(x)
y = 56
y -= 3
print(y)
# Logic operators - and, or, not
a = 23
print( a < 40 and a < 2)
print( a < 40 or a < 2)
print( not (a < 40 or a < 2))

# Operator precedence - order in which operations get executed
result = (10+6/2-4)
print(result)
print(int(result))












